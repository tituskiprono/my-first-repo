DROP DATABASE IF EXISTS SAMPLE;

CREATE DATABASE IF NOT EXISTS SAMPLE;

USE SAMPLE;

DROP TABLE IF EXISTS CUSTOMER;
DROP TABLE IF EXISTS INVOICE;
DROP TABLE IF EXISTS LINE;



CREATE TABLE IF NOT EXISTS CUSTOMER(
CUS_CODE INTEGER PRIMARY KEY,
CUS_LNAME VARCHAR(50),
CUS_FNAME VARCHAR(50),
CUS_INITIAL VARCHAR(10),
CUS_AREA_CODE VARCHAR(10),
CUS_PHONE VARCHAR(50),
CUS_BALANCE DECIMAL(10,2)
);

CREATE TABLE IF NOT EXISTS INVOICE(
INV_NUMBER INTEGER,
CUS_CODE INTEGER,
INV_DATE DATE,
PRIMARY KEY (INV_NUMBER,CUS_CODE),
FOREIGN KEY (CUS_CODE) REFERENCES CUSTOMER(CUS_CODE)
);

CREATE TABLE IF NOT EXISTS LINE(
INV_NUMBER INTEGER,
LINE_NUMBER INTEGER,
LINE_UNITS INTEGER,
LINE_PRICE DECIMAL(10,2),
PRIMARY KEY (INV_NUMBER,LINE_NUMBER),
FOREIGN KEY (INV_NUMBER) REFERENCES INVOICE(INV_NUMBER)
);


